from qtpy import QtCore

# =============================================================================
# 유틸리티
# =============================================================================
def qdate_to_yyyymmdd(qdate: QtCore.QDate) -> str:
    """QDate → 'YYYYMMDD' 문자열"""
    return qdate.toString("yyyyMMdd")


def qdate_from_yyyy_mm_dd(s: str) -> QtCore.QDate:
    """'yyyy-MM-dd' → QDate (빈 문자열/무효면 오늘)"""
    if not s:
        return QtCore.QDate.currentDate()
    d = QtCore.QDate.fromString(s, "yyyy-MM-dd")
    return d if d.isValid() else QtCore.QDate.currentDate()
